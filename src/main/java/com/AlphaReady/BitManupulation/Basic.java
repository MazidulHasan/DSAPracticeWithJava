package com.AlphaReady.BitManupulation;

public class Basic {
    public static void main(String[] args) {
        System.out.println("And Oparator : " + (5 & 6));
        System.out.println("OR Oparator : " + (5 | 6));
        System.out.println("XOR Oparator : " + (5 ^ 6));
        System.out.println("Binary 1's Oparator : " + (~5));
        System.out.println("Binary Left Shift : " + (5<<2));
        System.out.println("Binary Right Shift : " + (5>>2));
    }
}
